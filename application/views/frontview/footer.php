    function shorten_text(text) {
        if (!text) return "";
        let cleanText = text.replace(/<\/?[^>]+(>|$)/g, ""); 
        let result = jQuery.trim(cleanText).substring(0, 90).split(" ").slice(0, -1).join(" ") + "...";
        return result;
    }

    function imgviewer() {
        $('.image_container').on('click', '[data-toggle="lightbox"]', function(event) {
            event.preventDefault();
            $(this).ekkoLightbox();
        });
    }

    function safeParseJSON(data) {
        if (typeof data === 'object') { return data; }
        try { return JSON.parse(data); } 
        catch (e) { console.error("JSON Error", e); return []; }
    }

    // ============================================
    // 1. UPDATE FETCH BLOG (STRUKTUR MODERN)
    // ============================================
   function fetchblog() {
      $.ajax({
         url: "<?php echo base_url(); ?>blog/blog_content_list",
         success: function(result) {
            let decode_result = safeParseJSON(result);
            let url_link = "<?php echo base_url() ?>";
            
            $('#bloglist_container').empty();

            if(decode_result.length === 0){
                $('#bloglist_container').html('<div class="col-12 text-center py-5">Belum ada artikel.</div>');
                $('.loaderse').fadeOut();
                return;
            }

            decode_result.forEach(function(item) {
               let result_url = url_link + 'artikel/' + item.slug;
               let thumb = (item.image_path && item.image_path !== "") ? item.image_path : "thumb.png";
               let image_path = "<?php echo base_url() ?>assets/blog/thumb_img/" + thumb;

               // Format angka & tanggal
               let v_count = item.visitors ? parseInt(item.visitors).toLocaleString('id-ID') : 0;
               let view_count = item.views ? parseInt(item.views).toLocaleString('id-ID') : 0;
               let dateObj = new Date(item.date_created);
               let formattedDate = !isNaN(dateObj.getTime()) 
                                ? dateObj.toLocaleDateString('id-ID', { day: 'numeric', month: 'short', year: 'numeric' })
                                : item.date_created;

               let adapter = `
                  <div class="col-md-4 col-lg-4 col-sm-6 mb-4 d-flex align-items-stretch animate-up">
                      <div class="card modern-card w-100 mb-4">
                          <a href="${result_url}" class="blog-img-wrapper">
                              <img src="${image_path}" class="blog-img" alt="${item.title}"
                                   onerror="this.src='<?php echo base_url() ?>assets/blog/thumb_img_default/thumb.png'">
                          </a>
                          
                          <div class="card-body d-flex flex-column p-4">
                              <h5 class="blog-title">
                                  <a href="${result_url}" style="text-decoration:none; color:inherit;">${item.title}</a>
                              </h5>
                              <p class="card-text text-muted mb-4" style="font-size:0.9rem; flex-grow: 1;">
                                  ${shorten_text(item.content || '')}
                              </p>
                              
                              <div class="blog-footer w-100">
                                  <div class="d-flex align-items-center">
                                      <i class="fa fa-calendar-alt mr-2 text-success"></i> ${formattedDate}
                                  </div>
                                  <div class="d-flex align-items-center">
                                      <span title="Visitors" class="mr-3 d-flex align-items-center">
                                          <i class="fa fa-user mr-1" style="color: #27ae60;"></i> ${v_count}
                                      </span>
                                      <span title="Views" class="d-flex align-items-center">
                                          <i class="fa fa-eye mr-1" style="color: #2980b9;"></i> ${view_count}
                                      </span>
                                  </div>
                              </div>
                          </div>
                      </div>
                  </div>`;
               
               $('#bloglist_container').append(adapter);
            });

            $('.loaderse').fadeOut(300);
            $('.containerblog').fadeIn(50);
         },
         error: function(xhr, status, error) { console.error("Gagal load blog:", error); $('.loaderse').hide(); }
      });
   }

   function fetchblog_search(keyword) {
      $.ajax({
         method: 'get',
         url: "<?php echo base_url(); ?>blog/blog_content_list_search?keyword=" + keyword,
         success: function(result) {
            let decode_result = safeParseJSON(result);
            let url_link = "<?php echo base_url() ?>";
            $('#bloglist_container').empty(); 

            if(decode_result.length === 0){
                $('#bloglist_container').html('<div class="col-12 text-center py-5">Artikel tidak ditemukan.</div>');
                $('.loaderse').fadeOut(); return;
            }

            decode_result.forEach(function(item) {
               let result_url = url_link + 'artikel/' + item.slug;
               let image_path = (item.image_path && item.image_path !== "") ? "<?php echo base_url() ?>assets/blog/thumb_img/" + item.image_path : "<?php echo base_url(); ?>assets/blog/thumb_img_default/thumb.png";
               
               // Gunakan template yang sama persis dengan fetchblog agar konsisten
               let adapter = `
                  <div class="col-md-4 col-lg-4 col-sm-6 mb-4 d-flex align-items-stretch animate-up">
                      <div class="card modern-card w-100 mb-4">
                          <a href="${result_url}" class="blog-img-wrapper">
                              <img src="${image_path}" class="blog-img" alt="${item.title}">
                          </a>
                          <div class="card-body d-flex flex-column p-4">
                              <h5 class="blog-title">
                                  <a href="${result_url}" style="text-decoration:none; color:inherit;">${item.title}</a>
                              </h5>
                              <p class="card-text text-muted mb-4" style="flex-grow: 1;">${shorten_text(item.content || '')}</p>
                              
                              <div class="blog-footer w-100">
                                  <div><i class="fa fa-calendar-alt mr-2 text-success"></i> ${item.date_created}</div>
                                  <div><a href="${result_url}" class="text-success font-weight-bold">Baca <i class="fa fa-arrow-right"></i></a></div>
                              </div>
                          </div>
                      </div>
                  </div>`;
               $('#bloglist_container').append(adapter);
            });
            $('.loaderse').fadeOut(300);
            $('.containerblog').fadeIn(50);
         }
      });
   }

