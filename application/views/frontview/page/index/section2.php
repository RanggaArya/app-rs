document.addEventListener("DOMContentLoaded", function () {
    // Cari semua elemen yang mau dianimasikan
    const animatedItems = document.querySelectorAll('.ppt-item');

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            // Jika elemen masuk ke layar
            if (entry.isIntersecting) {
                entry.target.classList.add('visible');
            } 
            // Opsional: Jika ingin animasi ulang saat scroll naik (seperti PPT bolak balik)
            // Hapus tanda komentar di baris bawah ini jika ingin efeknya berulang terus:
            // else { entry.target.classList.remove('visible'); } 
        });
    }, {
        threshold: 0.1 // Animasi mulai saat 10% elemen terlihat
    });

    animatedItems.forEach(item => {
        observer.observe(item);
    });
});
</script>

